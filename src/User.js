export default class User {
	constructor(name, age, occupation, state, gender, image) {
		this.Name = name;
		this.Age = age;
		this.Occupation = occupation;
		this.State = state;
		this.Gender = gender;
		this.Image = image;
	}

	Name = "";
	Age = 0;
	Occupation = "";
	State = "";
	Gender = "";
	Image = "";
}
